I just downloaded the new free iOS game ◊link["http://itunes.apple.com/us/app/instagamer/id501880482?mt=8&ign-mpt=uo%3D4"]{InstaGamer} and its retro click-beep snippet of a sound effect is just ◊emph{exactly} what I’m looking for in a text alert tone. So I made it into a ring tone for the iPhone. ◊link["https://docs.google.com/open?id=0B9SDJ22NRBkrWjlZXzhjWjlTTEM3S3NvQ3JFOFQ1dw"]{Download it here}, then load it into iTunes and sync your phone.

Backstory: I’ve long been in search of the perfect “text alert” tone for my iPhone. The ones that come with the iPhone all seem too long; to me, a good text alert tone lasts a half second or less. You potentially end up hearing it dozens to hundreds of times a day, so all you need is a slippy little audio cue, not a prolonged gag or music riff.

